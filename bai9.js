let a = 5;
let b = "5";

// So sánh giá trị của a và b. Kết quả là true vì giá trị giống nhau sau khi chuyển đổi kiểu tự động.
console.log(a == b);

// So sánh giá trị và kiểu dữ liệu của a và b. Kết quả là false vì kiểu dữ liệu khác nhau.
console.log(a === b);

// So sánh giá trị của a và b. Kết quả là false vì giá trị giống nhau.
console.log(a != b);

// So sánh giá trị và kiểu dữ liệu của a và b. Kết quả là true vì giá trị giống nhau nhưng kiểu dữ liệu khác nhau.
console.log(a !== b);